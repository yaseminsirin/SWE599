def iter_demo_job_dicts() -> list[dict[str, Any]]:
    now = timezone.now()
    jobs: list[dict[str, Any]] = []
    for spec in _specs():
        location_text = (
            f"{spec.city}, {spec.country}" if spec.city != "Remote" else f"Remote · {spec.country}"
        )
        jobs.append(
            {
                "slug": spec.slug,
                "title": spec.title,
                "company_name": spec.company_name,
                "category_normalized": spec.category,
                "city": spec.city if spec.city != "Remote" else "",
                "country": spec.country,
                "location_text": location_text,
                "is_remote": spec.is_remote,
                "employment_type": spec.employment_type,
                "description_clean": spec.description,
                "description_raw": spec.description,
                "salary_min": spec.salary_min,
                "salary_max": spec.salary_max,
                "posted_at": now - timedelta(days=spec.days_ago),
                "job_url": f"{DEMO_BASE_URL}/{spec.slug}",
            }
        )
    return jobs
